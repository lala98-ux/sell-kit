/* <div id="sellkit-legal" data-legal="terms|privacy"></div> 에 주입 */
(function () {
  function render() {
    var mount = document.getElementById('sellkit-legal');
    if (!mount) return;
    var key = mount.getAttribute('data-legal');
    var doc = SELLKIT_LEGAL[key];
    if (!doc) return;
    mount.innerHTML =
      '<span class="legal-eyebrow">LEGAL</span>' +
      '<h1>' + doc.title + '</h1>' +
      '<p class="legal-updated">' + doc.updated + '</p>' +
      '<p class="legal-intro">' + doc.intro + '</p>' +
      doc.body;
  }
  if (document.readyState === 'loading') document.addEventListener('DOMContentLoaded', render);
  else render();
})();

/* ---------- 모달 ---------- */
function sellkitOpenLegal(key) {
  var doc = SELLKIT_LEGAL[key];
  if (!doc) return;

  var overlay = document.getElementById('sellkit-legal-modal');
  if (!overlay) {
    overlay = document.createElement('div');
    overlay.id = 'sellkit-legal-modal';
    overlay.className = 'legal-modal';
    overlay.innerHTML =
      '<div class="legal-modal-box" role="dialog" aria-modal="true">' +
        '<div class="legal-modal-head">' +
          '<h2 id="legalModalTitle"></h2>' +
          '<button type="button" class="legal-modal-close" aria-label="닫기">&times;</button>' +
        '</div>' +
        '<div class="legal-modal-body" id="legalModalBody"></div>' +
      '</div>';
    document.body.appendChild(overlay);

    function close() { overlay.classList.remove('open'); document.body.style.overflow = ''; }
    overlay.addEventListener('click', function (e) {
      if (e.target === overlay || e.target.closest('.legal-modal-close')) close();
    });
    document.addEventListener('keydown', function (e) {
      if (e.key === 'Escape') close();
    });
  }

  overlay.querySelector('#legalModalTitle').textContent = doc.title;
  overlay.querySelector('#legalModalBody').innerHTML =
    '<p class="legal-updated">' + doc.updated + '</p>' +
    '<p class="legal-intro">' + doc.intro + '</p>' +
    doc.body;
  overlay.querySelector('.legal-modal-body').scrollTop = 0;
  overlay.classList.add('open');
  document.body.style.overflow = 'hidden';
}
